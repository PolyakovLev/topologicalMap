#include "libft.h"

void	ft_strstrdel(char **a)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (a[j])
		j++;
	while (i < j)
	{
		free(a[i]);
		i++;
	}
	free(a);
}
